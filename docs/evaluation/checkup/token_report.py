#!/usr/bin/env python3
"""token_report.py — static instruction-footprint model for the plugin checkup.

Outputs are ESTIMATED STATIC INSTRUCTION FOOTPRINT, not measured tokens:
`est_tokens(bytes) = ceil(bytes/4)` with a stated ~±25% band. Bytes are kept
alongside. The primary metric (per the checkup decision) is the plugin's added
footprint PER CONTEXT; the reviewer/validator agents run in SEPARATE contexts,
so their footprint is never added to the main context's peak. Task 8 fills the
empirical (plugin-on vs baseline) section; Task 11 fills billed uncached input.
"""
import math
import os
import sys

# Read the pinned snapshot; fall back to the primary tree (verified identical).
_HERE = os.path.dirname(os.path.abspath(__file__))
_PRIMARY = os.path.abspath(os.path.join(_HERE, "..", "..", ".."))
_SNAP = os.path.join(_PRIMARY, ".claude", "worktrees", "checkup-snapshot")
ROOT = _SNAP if os.path.isdir(_SNAP) else _PRIMARY

CMD = "commands"
SK = "skills/managing-research-plans"

# Surfaces: (path, stratum, separate_context). Sizes measured live.
COMMANDS = ["adopt", "board", "init", "models", "plan", "renew", "report",
            "results", "review", "sync"]
SURFACES = (
    [("%s/%s.md" % (CMD, c), "per-invocation", False) for c in COMMANDS] +
    [("%s/SKILL.md" % SK, "session-start", False),
     ("%s/references/plan-rubric.md" % SK, "per-dispatch", False),
     ("%s/templates/execution-plan.md" % SK, "authoring", False),
     ("%s/templates/agents/rp-plan-reviewer.md" % SK, "per-dispatch", True),
     ("%s/templates/agents/rp-board-reviewer.md" % SK, "per-dispatch", True),
     ("%s/templates/agents/rp-results-validator.md" % SK, "per-dispatch", True)]
)

# Flows derived by READING the command bodies (citations in comments), not
# hard-coded. Each flow is a list of contexts; each context is a list of paths.
FLOWS = {
    "bare /board (plain open)": [
        ["%s/board.md" % CMD],                       # one context, whole file
    ],
    "/plan -> review -> board (sign-off chain)": [
        # main session across the chain: plan.md loads the template (plan.md:26)
        # and auto-chains /review then /board (plan.md:34)
        ["%s/plan.md" % CMD, "%s/templates/execution-plan.md" % SK,
         "%s/review.md" % CMD, "%s/board.md" % CMD],
        # reviewer dispatched into a SEPARATE context (review.md:11)
        ["%s/templates/agents/rp-plan-reviewer.md" % SK,
         "%s/references/plan-rubric.md" % SK],
    ],
    "/sync -> /results -> /board": [
        ["%s/sync.md" % CMD, "%s/results.md" % CMD, "%s/board.md" % CMD],
        # results validator dispatched into a separate context (results.md:21)
        ["%s/templates/agents/rp-results-validator.md" % SK],
    ],
    "/review (standalone)": [
        ["%s/review.md" % CMD],
        ["%s/templates/agents/rp-plan-reviewer.md" % SK,
         "%s/references/plan-rubric.md" % SK],
    ],
}

BAND = 0.25  # est_tokens uncertainty band, +/-


def est_tokens(byte_total):
    return math.ceil(byte_total / 4)


def _ctx_footprint(ctx, sizes):
    return est_tokens(sum(sizes[s] for s in ctx))


def peak_context(flow, sizes):
    return max(_ctx_footprint(ctx, sizes) for ctx in flow)


def cumulative(flow, sizes):
    return sum(_ctx_footprint(ctx, sizes) for ctx in flow)


def _selftest():
    sizes = {"A": 400, "B": 160, "C": 160}
    flow = [["A"], ["B", "C"]]                      # ctx0: A; ctx1: B+C
    assert est_tokens(400) == 100, est_tokens(400)
    assert peak_context(flow, sizes) == 100, peak_context(flow, sizes)   # max(100, 80)
    assert cumulative(flow, sizes) == 180, cumulative(flow, sizes)       # 100 + 80
    print("selftest OK")


def _fbytes(path):
    return os.path.getsize(os.path.join(ROOT, path))


def _desc_bytes(path):
    """Bytes of the frontmatter `description:` value (the always-on cost)."""
    full = os.path.join(ROOT, path)
    with open(full, encoding="utf-8") as fh:
        for line in fh:
            if line.startswith("description:"):
                return len(line[len("description:"):].strip().encode("utf-8"))
    return 0


def _report():
    out = ["# Token report — static instruction footprint",
           "",
           "Generated by `token_report.py`. Numbers are **estimated static "
           "footprint** (`ceil(bytes/4)`, ~+/-25%%), not measured tokens; bytes "
           "kept alongside. Snapshot: `%s`." % os.path.basename(ROOT),
           ""]

    # 1. Always-on floor
    floor_srcs = [("%s/%s.md" % (CMD, c)) for c in COMMANDS] + \
                 [("%s/SKILL.md" % SK)]
    floor_bytes = sum(_desc_bytes(p) for p in floor_srcs)
    out += ["## 1. Always-on floor (loaded every session, every project)",
            "",
            "The `description:` lines of the 10 commands + the skill — the only "
            "unconditional cost.",
            "",
            "| source | description bytes |",
            "|---|---|"]
    for p in floor_srcs:
        out.append("| %s | %d |" % (p.split("/")[-1], _desc_bytes(p)))
    out += ["| **total** | **%d B ~ %d est-tokens** |" %
            (floor_bytes, est_tokens(floor_bytes)), ""]

    # 2. Per-surface static footprint
    out += ["## 2. Per-surface static footprint",
            "",
            "| surface | bytes | est-tokens | stratum | separate context |",
            "|---|---|---|---|---|"]
    for path, stratum, sep in sorted(SURFACES, key=lambda s: -_fbytes(s[0])):
        b = _fbytes(path)
        out.append("| %s | %d | %d | %s | %s |" %
                   (path.replace(SK + "/", ""), b, est_tokens(b), stratum,
                    "yes" if sep else "no"))
    out.append("")

    # 3. Per-flow peak vs cumulative
    out += ["## 3. Per-flow: peak single context vs cumulative",
            "",
            "`peak` = the most instruction footprint in any ONE context; "
            "`cumulative` = summed across all contexts (incl. separate "
            "subagent contexts). They differ whenever a flow dispatches.",
            "",
            "| flow | peak est-tokens | cumulative est-tokens | contexts |",
            "|---|---|---|---|"]
    sizes_all = {p: _fbytes(p) for p, _, _ in SURFACES}
    for name, contexts in FLOWS.items():
        pk = peak_context(contexts, sizes_all)
        cu = cumulative(contexts, sizes_all)
        out.append("| %s | %d | %d | %d |" % (name, pk, cu, len(contexts)))
    out += ["",
            "_Peak != cumulative confirms the separate-context modeling: the "
            "reviewer/validator footprint is not main-context pressure._",
            ""]

    # 4/5. Stubs for the empirical + billed sections
    out += ["## 4. Empirical: plugin-on vs minimal-baseline (filled by Task 8)",
            "",
            "_Pending: the paired-run delta = the plugin's added instruction "
            "footprint per context, the honest anchor for section 2's estimate._",
            "",
            "## 5. Billed uncached input (filled by Task 11)",
            "",
            "_Pending: real per-flow billed input/cache-read from transcripts. "
            "Confound: total input mixes instructions + project data + history + "
            "tool output — this is the cost number, not a refinement of section 2._",
            ""]
    return "\n".join(out) + "\n"


if __name__ == "__main__":
    if "--selftest" in sys.argv:
        _selftest()
        sys.exit(0)
    if "--out" in sys.argv:
        dest = sys.argv[sys.argv.index("--out") + 1]
        with open(dest, "w", encoding="utf-8") as fh:
            fh.write(_report())
        print("wrote", dest)
        sys.exit(0)
    print(_report())
